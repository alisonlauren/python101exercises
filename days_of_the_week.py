day_week = int(input('Day (0-6)? '))
if day_week == 0: 
    print("It's Sunday!")
elif day_week == 1:
    print("It's Monday!")
elif day_week == 2:
    print("It's Tuesday!")
elif day_week == 3:
    print("It's Wednesday!")
elif day_week == 4:
    print("It's Thursday!")
elif day_week == 5:
    print("It's Friday, finally!")
elif day_week == 6:
    print("It's Saturday, the best day of all.")
else:
    print("That's not a day of the week bro")


