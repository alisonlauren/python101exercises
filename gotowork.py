day_week = int(input('Day (0-6)? '))
if day_week == 0: 
    print("Sleep in! It's the weekend!")
elif day_week == 1:
    print("Go to work, you're nearly late!")
elif day_week == 2:
    print("Go to work, why are you taking a quiz now? Get ready!")
elif day_week == 3:
    print("Go to work, or else you'll probably get fired.")
elif day_week == 4:
    print("It's Thursday, you know you work that day right.")
elif day_week == 5:
    print("Last day, Go to work!")
elif day_week == 6:
    print("It's Saturday, the best day of all. Sleep in")
else:
    print("That's not a day of the week bro")